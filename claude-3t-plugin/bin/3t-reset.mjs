#!/usr/bin/env node
// claude-3t reset — wipe a project's 3t scaffolding back to ZERO so /3t-init can
// run from scratch. Built for testing the init flow repeatedly, but also useful
// to anyone who wants a clean slate.
//
// DESTRUCTIVE. By design this is "true zero": it deletes ALL 3t artifacts AND
// docs/adr, including the COMMITTED files (CONTEXT.md, cold/). After it runs the
// project looks like 3t was never initialized here. Committed files remain
// recoverable via git (`git restore`); uncommitted ones are gone for good.
//
// Two-stage safety:
//   node bin/3t-reset.mjs            → DRY RUN. Lists exactly what would be
//                                      deleted/edited. Changes nothing.
//   node bin/3t-reset.mjs --confirm  → performs the reset.
//
// The skill (/3t-reset) is expected to run the dry run, show the user the plan,
// get an explicit yes, THEN run with --confirm. The flag is a second backstop so
// the script can never delete on an accidental invocation.
//
// Scope is the current working directory (the project root). Run from there.

import {
  existsSync, rmSync, readFileSync, writeFileSync, readdirSync, statSync,
} from "node:fs";
import { join } from "node:path";

const cwd = process.cwd();
const P = (...p) => join(cwd, ...p);
const confirm = process.argv.includes("--confirm") || process.argv.includes("--yes");

// ── targets ───────────────────────────────────────────────────────────────────

// Individual files scaffolded or generated by 3t (incl. legacy SONNET/HAIKU names
// so projects initialized before the executor/implementor rename clean up fully).
const FILES = [
  ".claude/context/MEMORY.md",
  ".claude/context/CONTEXT.md",
  ".claude/context/EXECUTOR_MEMORY.md",
  ".claude/context/IMPLEMENTOR_MEMORY.md",
  ".claude/context/SONNET_MEMORY.md",
  ".claude/context/HAIKU_MEMORY.md",
  ".claude/context/OVERRIDE_LOG.md",
  ".claude/context/3t-plugin-feedback.md",
  ".claude/context/cold/INDEX.md",
  ".claude/.3t-active",
  ".claude/.3t-disabled",
  ".claude/.3t-workflows",
  ".claude/CLAUDE.local.md",
];

// Directories removed wholesale (true zero). cold first, then context, then adr.
const DIRS = [
  ".claude/context/cold",
  ".claude/context",
  "docs/adr",
];

// ── helpers ───────────────────────────────────────────────────────────────────

// List any extra cold topic files (cold/*.md beyond INDEX.md) so the dry run is
// honest about what the directory wipe will take.
function coldExtras() {
  const dir = P(".claude/context/cold");
  if (!existsSync(dir)) return [];
  try {
    return readdirSync(dir)
      .filter((f) => f.endsWith(".md") && f !== "INDEX.md")
      .map((f) => `.claude/context/cold/${f}`);
  } catch { return []; }
}

function adrContents() {
  const dir = P("docs/adr");
  if (!existsSync(dir)) return [];
  try { return readdirSync(dir).map((f) => `docs/adr/${f}`); } catch { return []; }
}

// Strip the 3t block from .gitignore — from the "# Claude Code 3-tier" marker
// through the last 3t entry (.3t-workflows). Returns {found, newText}.
function stripGitignore(text) {
  const lines = text.split("\n");
  const start = lines.findIndex((l) => l.includes("Claude Code 3-tier"));
  if (start === -1) return { found: false, newText: text };
  // Find the end: last contiguous 3t-related line from start.
  let end = start;
  for (let i = start; i < lines.length; i++) {
    const l = lines[i];
    if (
      l.includes("Claude Code 3-tier") ||
      l.includes("3-tier transient flags") ||
      l.includes(".claude/context/") ||
      l.includes(".claude/CLAUDE.local.md") ||
      l.includes(".claude/.3t-") ||
      l.trim() === ""
    ) {
      // include blank lines only if they sit between 3t lines; stop at first
      // blank that is followed by a non-3t line.
      if (l.trim() === "") {
        const next = lines[i + 1] || "";
        const nextIs3t =
          next.includes(".claude/") || next.includes("Claude Code 3-tier") ||
          next.includes("3-tier transient");
        if (!nextIs3t) break;
      }
      end = i;
    } else {
      break;
    }
  }
  // Drop a single leading blank line before the block, if present.
  let from = start;
  if (from > 0 && lines[from - 1].trim() === "") from -= 1;
  lines.splice(from, end - from + 1);
  return { found: true, newText: lines.join("\n") };
}

// Remove the advisorModel key 3t-init added. Leave every other key (incl. model,
// which 3t-init never sets). Returns {found, changed-json-or-null}.
function stripSettings(text) {
  let obj;
  try { obj = JSON.parse(text); } catch { return { found: false, json: null, parseError: true }; }
  if (!Object.prototype.hasOwnProperty.call(obj, "advisorModel")) {
    return { found: false, json: null };
  }
  delete obj.advisorModel;
  return { found: true, json: JSON.stringify(obj, null, 2) + "\n" };
}

// ── plan ──────────────────────────────────────────────────────────────────────

const filesToDelete = FILES.filter((f) => existsSync(P(f)));
const extras = coldExtras();
const adr = adrContents();
const dirsToRemove = DIRS.filter((d) => existsSync(P(d)));

const gitignorePath = P(".gitignore");
const hasGitignoreBlock =
  existsSync(gitignorePath) && readFileSync(gitignorePath, "utf8").includes("Claude Code 3-tier");

const settingsPath = P(".claude/settings.json");
let settingsHasKey = false;
if (existsSync(settingsPath)) {
  try { settingsHasKey = "advisorModel" in JSON.parse(readFileSync(settingsPath, "utf8")); } catch { /* ignore */ }
}

const nothing =
  !filesToDelete.length && !extras.length && !adr.length && !dirsToRemove.length &&
  !hasGitignoreBlock && !settingsHasKey;

// ── dry run ───────────────────────────────────────────────────────────────────

const bar = "─".repeat(64);

if (!confirm) {
  console.log(`\n3t RESET — DRY RUN (nothing deleted)`);
  console.log(bar);
  console.log(`Project: ${cwd}`);
  if (nothing) {
    console.log(`\nNo 3t artifacts found here. Project is already at zero.`);
    console.log(bar);
    process.exit(0);
  }
  console.log(`\nWould DELETE these files:`);
  [...filesToDelete, ...extras, ...adr].forEach((f) => console.log(`  - ${f}`));
  if (dirsToRemove.length) {
    console.log(`\nWould REMOVE these directories (if empty after file deletion):`);
    dirsToRemove.forEach((d) => console.log(`  - ${d}/`));
  }
  if (hasGitignoreBlock) console.log(`\nWould STRIP the 3t block from .gitignore`);
  if (settingsHasKey) console.log(`Would REMOVE "advisorModel" from .claude/settings.json`);
  console.log(`\n⚠  DESTRUCTIVE — this is "true zero" incl. docs/adr and committed`);
  console.log(`   CONTEXT.md / cold/. Committed files are recoverable via git restore.`);
  console.log(`\nTo proceed:  node "<plugin>/bin/3t-reset.mjs" --confirm`);
  console.log(bar);
  process.exit(0);
}

// ── execute ───────────────────────────────────────────────────────────────────

const done = [];

for (const f of [...filesToDelete, ...extras, ...adr]) {
  try { rmSync(P(f), { force: true }); done.push(`deleted ${f}`); } catch (e) { done.push(`FAILED ${f}: ${e.message}`); }
}
for (const d of dirsToRemove) {
  try { rmSync(P(d), { recursive: true, force: true }); done.push(`removed ${d}/`); } catch (e) { done.push(`FAILED ${d}/: ${e.message}`); }
}

if (hasGitignoreBlock) {
  const { found, newText } = stripGitignore(readFileSync(gitignorePath, "utf8"));
  if (found) { writeFileSync(gitignorePath, newText); done.push(`stripped 3t block from .gitignore`); }
}

if (settingsHasKey) {
  const { found, json } = stripSettings(readFileSync(settingsPath, "utf8"));
  if (found && json) { writeFileSync(settingsPath, json); done.push(`removed advisorModel from .claude/settings.json`); }
}

console.log(`\n3t RESET — COMPLETE`);
console.log(bar);
console.log(`Project: ${cwd}`);
if (!done.length) {
  console.log(`Nothing to do — already at zero.`);
} else {
  done.forEach((d) => console.log(`  ${d}`));
}
console.log(`\nProject is back to zero. Run /claude-3t:3t-init to scaffold fresh,`);
console.log(`then restart Claude so the SessionStart hook picks up the new marker.`);
console.log(bar);
